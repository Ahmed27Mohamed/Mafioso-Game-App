    fun checkEndCondition(): Boolean {

        val murderersLeft = players.count {
            it.role == Role.MURDERER && !it.isEliminated
        }

        val innocentsLeft = players.count {
            it.role == Role.INNOCENT && !it.isEliminated
        }

        return when {
            murderersLeft == 0 -> true
            murderersLeft > innocentsLeft -> true
            else -> false
        }
    }

